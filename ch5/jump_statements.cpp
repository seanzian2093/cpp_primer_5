#include <iostream>
#include <string>
#include <vector>

using namespace std;

int main()
{
    // break statement
    // - terminates the nearest enclosing while, do while, for or switch statement, i.e., terminate the loop
    // - execution resumes at the statement immediately following the terminated statement
    // - can appear in
        // - while
        // - do while
        // - for
        // - switch

    // continue statement
    // - terminates the current iteration of the nearest enclosing loop and
    // - immediately begins the next iteration.
    // - can appear in
        // - while
        // - do while
        // - for

    // goto statement
    // - unconditional jump from goto to another statement in the same function
    // - syntax
        // - goto label;
    // - a labeled statement - preceded by an identifier followed be a colon
        // - the label is end
        // - end: return;
}